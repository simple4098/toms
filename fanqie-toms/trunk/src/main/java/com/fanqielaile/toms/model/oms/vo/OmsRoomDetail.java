package com.fanqielaile.toms.model.oms.vo;

public class OmsRoomDetail {
	private String roomDate;
	private String roomPrice;
	private String priRoomPrice;
	private Integer roomNum;
	public String getRoomDate() {
		return roomDate;
	}
	public void setRoomDate(String roomDate) {
		this.roomDate = roomDate;
	}
	public String getRoomPrice() {
		return roomPrice;
	}
	public void setRoomPrice(String roomPrice) {
		this.roomPrice = roomPrice;
	}
	public String getPriRoomPrice() {
		return priRoomPrice;
	}
	public void setPriRoomPrice(String priRoomPrice) {
		this.priRoomPrice = priRoomPrice;
	}
	public Integer getRoomNum() {
		return roomNum;
	}
	public void setRoomNum(Integer roomNum) {
		this.roomNum = roomNum;
	}
	
}

